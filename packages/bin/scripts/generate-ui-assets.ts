/**
 * Emit `src/http/ui-assets.generated.ts` — the embedded-UI manifest (ADR
 * 0013): one Bun file-type import per file in `packages/ui/dist`, keyed by
 * URL path, so `bun build --compile` embeds the console in the binary.
 *
 * No dist (fresh clone, UI not built yet) emits an empty map so the binary
 * package always typechecks. The output is gitignored; `check`/`test`/`build`
 * regenerate it first.
 */
import { existsSync, mkdirSync, readdirSync, writeFileSync } from 'node:fs';
import { join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = fileURLToPath(new URL('.', import.meta.url));
const distDir = join(here, '..', '..', 'ui', 'dist');
const outFile = join(here, '..', 'src', 'http', 'ui-assets.generated.ts');

/** content-type by extension — only what a Vite/PWA build actually emits. */
const TYPES: Record<string, string> = {
  html: 'text/html; charset=utf-8',
  js: 'text/javascript; charset=utf-8',
  css: 'text/css; charset=utf-8',
  svg: 'image/svg+xml',
  webmanifest: 'application/manifest+json',
  json: 'application/json',
  png: 'image/png',
  ico: 'image/x-icon',
  txt: 'text/plain; charset=utf-8',
  woff2: 'font/woff2',
  woff: 'font/woff',
  map: 'application/json',
};

function contentType(file: string): string {
  const ext = file.slice(file.lastIndexOf('.') + 1).toLowerCase();
  return TYPES[ext] ?? 'application/octet-stream';
}

function listFiles(dir: string): string[] {
  return readdirSync(dir, { recursive: true, withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => join(entry.parentPath, entry.name))
    .toSorted();
}

const header =
  '// GENERATED by packages/bin/scripts/generate-ui-assets.ts — do not edit; gitignored.\n' +
  '// Maps URL paths to embedded UI files (ADR 0013). Empty when packages/ui/dist is absent.\nimport type { UiAssetMap } from "./static";\n';

let body: string;
if (!existsSync(distDir)) {
  body = '\nexport const UI_ASSETS: UiAssetMap = {};\n';
} else {
  const files = listFiles(distDir);
  const imports: string[] = [];
  const entries: string[] = [];
  files.forEach((path, i) => {
    const rel = relative(distDir, path).split('\\').join('/');
    const urlPath = `/${rel}`;
    // hashed build output is immutable; the shell (html, sw, manifest, icons) is not
    const immutable = rel.startsWith('assets/');
    imports.push(`import f${String(i)} from "../../../ui/dist/${rel}" with { type: "file" };`);
    entries.push(
      `  ${JSON.stringify(urlPath)}: { file: f${String(i)}, type: ${JSON.stringify(contentType(rel))}, immutable: ${String(immutable)} },`,
    );
  });
  body = `${imports.join('\n')}\n\nexport const UI_ASSETS: UiAssetMap = {\n${entries.join('\n')}\n};\n`;
}

mkdirSync(join(outFile, '..'), { recursive: true });
writeFileSync(outFile, `${header}${body}`);
const count = body.includes('file:') ? body.split('file:').length - 1 : 0;
console.log(`ui-assets: ${String(count)} asset(s) → ${relative(process.cwd(), outFile)}`);
